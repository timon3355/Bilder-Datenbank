	<h2>Q-Drive</h2>
	<p>Die Webseite «www.Q-Drive.xx» bietet dir eine Möglichkeit Dateien
		aller Art hochzuladen und mit deinen Freunden oder der ganzen Welt zu
		Teilen. Auch ohne Anmeldung ist es Dir möglich die öffentlichen
		freigeschalteten Dateien herunterzuladen. Durch die Suchfunktion ist
		es ganz einfach das Gewünschte Dokument zu finden. Erstelle ein
		Account und es kann los gehen. Völlig anonym und frei, ohne jegliche
		Gefahren für die Privatsphäre und innerhalb von ein paar Klicks
		gemacht. Verwaltet deine Dateien und gib anderen Accounts Zugriff auf
		sie! Wenn dir dein Benutzername, Passwort nicht mehr gefällt, ändere
		es einfach und ohne grosse Mühe.</p>
